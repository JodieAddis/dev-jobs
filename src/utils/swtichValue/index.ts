export const BgForLogos = (company: string) => {
  switch (company.toLowerCase()) {
    case "scoot":
      return "scoot_logo";
    case "blogr":
      return "blogr_logo";
    case "vector":
      return "vector_logo";
    case "office lite":
      return "office_logo";
    case "pod":
      return "pod_logo";
    case "creative":
      return "creative_logo";
    case "pomodoro":
      return "pomodoro_logo";
    case "maker":
      return "maker_logo";
    case "coffeeroasters":
      return "coffee_logo";
    case "mastercraft":
      return "mastercraft_logo";
    case "crowfund":
      return "crowfund_logo";
    case "typemaster":
      return "typemaster_logo";
    case "crowdfund":
      return "crowdfund_logo";
  }
};
